class VGGFeatures(nn.Module):
    def __init__(self, layers):
        super().__init__()
        vgg = models.vgg16(weights=models.VGG16_Weights.DEFAULT).features
        self.slices = nn.ModuleList()
        prev = 0
        for l in layers:
            self.slices.append(nn.Sequential(*list(vgg.children())[prev:l+1]))
            prev = l + 1
        for p in self.parameters():
            p.requires_grad_(False)
        mean = torch.tensor([0.485, 0.456, 0.406]).view(1,3,1,1)
        std  = torch.tensor([0.229, 0.224, 0.225]).view(1,3,1,1)
        self.register_buffer("mean", mean)
        self.register_buffer("std",  std)

    def forward(self, x):
        # x: [B, 3, H, W] in [-1,1]
        x = (x * 0.5 + 0.5 - self.mean) / self.std
        feats = []
        for s in self.slices:
            x = s(x)
            feats.append(x)
        return feats


def gram_matrix(feat):
    B, C, H, W = feat.shape
    f = feat.view(B, C, H*W)
    return torch.bmm(f, f.transpose(1,2)) / (C * H * W)


def gram_loss(feats_pred, feats_target):
    loss = 0.0
    for fp, ft in zip(feats_pred, feats_target):
        loss = loss + F.mse_loss(gram_matrix(fp), gram_matrix(ft))
    return loss


def swd_loss(feats_pred, feats_target, n_proj=64):
    """
    Sliced Wasserstein Distance on VGG features — same as paper's D_Global.
    Projects features onto random directions and measures 1D Wasserstein distance.
    """
    loss = 0.0
    for fp, ft in zip(feats_pred, feats_target):
        B, C, H, W = fp.shape
        fp_flat = fp.view(B, C, -1).mean(0).T   # [H*W, C]
        ft_flat = ft.view(B, C, -1).mean(0).T

        # Random projections
        proj = F.normalize(torch.randn(C, n_proj, device=fp.device), dim=0)
        fp_proj = fp_flat @ proj   # [H*W, n_proj]
        ft_proj = ft_flat @ proj

        fp_sorted = fp_proj.sort(dim=0).values
        ft_sorted = ft_proj.sort(dim=0).values
        loss = loss + F.mse_loss(fp_sorted, ft_sorted)
    return loss


