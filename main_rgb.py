def load_gif(path, size, n_frames):
    gif = Image.open(path)
    all_frames = []
    for i in range(gif.n_frames):
        gif.seek(i)
        frame = gif.convert("RGB")
        w, h = frame.size
        s = min(w, h)
        frame = frame.crop(((w-s)//2, (h-s)//2, (w+s)//2, (h+s)//2))
        frame = frame.resize((size, size), Image.LANCZOS)
        all_frames.append(np.array(frame, dtype=np.float32) / 255.0)

    indices = np.linspace(0, len(all_frames)-1, n_frames, dtype=int)
    frames  = np.stack([all_frames[i] for i in indices])
    frames  = frames.transpose(0, 3, 1, 2) * 2.0 - 1.0
    return torch.from_numpy(frames).to(device)   # [N, 3, H, W] in [-1,1]

