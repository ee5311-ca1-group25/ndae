    axes[0].grid(alpha=0.3)

    axes[1].plot(history["iter"], history["gram"], label="Gram", alpha=0.8)
    axes[1].plot(history["iter"], history["swd"],  label="SWD",  alpha=0.8)
    axes[1].set_title("Loss components")
    axes[1].set_xlabel("Iteration")
    axes[1].legend()
    axes[1].grid(alpha=0.3)

    plt.tight_layout()
    plt.show()


